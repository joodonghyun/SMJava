/*
* 바이트 타입
* 1바이트 단위: -128~127 값을 저장할수 있는 공간
*/

public class ByteType {

	public static void main(String[] args) {
		byte zero = 0;
		byte minus = -128;
		byte plus = 127;
		byte abc = 123;
		
		
		System.out.println("zero:" + zero);
		System.out.println("minus:" + minus);
		System.out.println(plus);
		System.out.println(abc);
		System.out.println();
	}

}
